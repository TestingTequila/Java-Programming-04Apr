package com.janbaskdemo.stringsmath;

public class IncrementDecrementMisc {
    public static void main(String[] args) {

        int a = 100;
        System.out.println(a++);//100
        System.out.println(a);  //101


        int b = -99;
        System.out.println(++b);
        System.out.println(b);

        int c = 123;
        System.out.println(--c);
        System.out.println(c);
    }
}
